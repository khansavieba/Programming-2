    public void populateArrayList(){
        try{
          FileInputStream file = new FileInputStream("order.txt");
          ObjectInputStream inputFile = new ObjectInputStream(file);
          
          boolean endOfFile = false;
          while(!endOfFile)
          {
             try
             {
               OrderSupplies.add((OrderSupply)inputFile.readObject());  
             } 
             catch(EOFException e)
             {
                endOfFile = true;
             }
             catch (Exception f) 
             {
               JOptionPane.showMessageDialog(null, f.getMessage());  
             }
          }//while
        inputFile.close(); 
        }//try
        catch(IOException e){
          JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void saveOrderToFile()
    {
        try
        {
            FileOutputStream file = new FileOutputStream("order.txt");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for(int i = 0; i < OrderSupplies.size(); i++)
            {
                outputFile.writeObject(OrderSupplies.get(i));
            }
            outputFile.close();
             JOptionPane.showMessageDialog(null, "Successfully Saved!");
             //this.dispose();
        }  
        catch(IOException e)
        {
           JOptionPane.showMessageDialog(null, e.getMessage());         
        }
    }
    
    public void addRowToJTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[4];
        for (int i = 0; i < OrderSupplies.size(); i++)
        {
            rowData[0] = OrderSupplies.get(i).getOrderKey();
            rowData[1] = OrderSupplies.get(i).getHospitalName();
            rowData[2] = OrderSupplies.get(i).getFruitOrder();
            rowData[3] = OrderSupplies.get(i).getAmount();
            
            model.addRow(rowData);
        }
    }

