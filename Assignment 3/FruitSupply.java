    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String searchKey = SearchBarText.getText().trim();
        int found = 0;
       
       
        for(int i = 0 ; i < OrderSupplies.size() ; i++  )
        {
          
            if (searchKey.equals(OrderSupplies.get(i).getOrderKey())) 
  
            {
                jTextArea1.setText("Order Key " + SearchBarText.getText().trim() + " Found!\n" + 
                    "Hospital Name\t\t: " + OrderSupplies.get(i).getHospitalName() + "\n"+
                    "Fruit Ordered\t\t: " + OrderSupplies.get(i).getFruitOrder() + "\n" +
                    "Amount of Fruit Ordered\t\t: " + OrderSupplies.get(i).getAmount()+ "\n"    );
                found++;
            }
         
        }//for
        
        if(found == 0)
        {
           jTextArea1.setText("Not Found!");

        }
      
      
    }//GEN-LAST:event_jButton1ActionPerformed

